package gob.mx.imss.mspad.oauth.exception;

public class Username3ReintentosException extends Throwable {
	private static final long serialVersionUID = -1666849832146548506L;

	public Username3ReintentosException() {
	}

	public Username3ReintentosException(String message) {
		super(message);
	}

	public Username3ReintentosException(Throwable cause) {
		super(cause);
	}

	public Username3ReintentosException(String message, Throwable cause) {
		super(message, cause);
	}

	public Username3ReintentosException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}
}
